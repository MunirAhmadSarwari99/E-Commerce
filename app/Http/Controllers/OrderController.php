    {
        $order = $request->input('order');

        foreach ($order as $key => $key){
            Order::create(['user_id' => Auth::user()->id,'cart_id' => $request->input('order')[$key],'orderNo' => rand(10,2000), 'total' => $request->input('inputToplam')]);
        }
        return back();
    }

